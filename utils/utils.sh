#!/bin/bash
##################################################################################
CUR_DIR=$(dirname `readlink -f "${BASH_SOURCE[0]}" 2>/dev/null||echo $0`)
. $CUR_DIR/settings.conf
. $CUR_DIR/dp-rmi-utils.sh
. $CUR_DIR/dp-xmi-utils.sh
##################################################################################
# log
##################################################################################
log_success() {
    MSG=$1

    echo -e $GREEN"$MSG"$NC
}
##################################################################################
# log
##################################################################################
log_error() {
    MSG=$1

    echo -e $RED"$MSG"$NC
}
##################################################################################
# log
##################################################################################
log_info() {
    MSG=$1

    echo -e $PURPLE"$MSG"$NC
}
##################################################################################
# log
##################################################################################
log_title() {
    MSG=$1

    echo -e $BLUE"$MSG"$NC
}
##################################################################################
# Calculates number of dynamic objects
##################################################################################
numOfObjects(){
    OBJ=$1

    SEQ=0
    while [ true ]; do
        CUR_OBJ="$(getIndirectValue $OBJ $SEQ)"
        if [ -z "$CUR_OBJ" ]; then
            echo $SEQ
            exit
        fi
        ((SEQ++))
    done
}
##################################################################################
# Get indirect variable value
##################################################################################
getIndirectValue(){
    PREFIX=$1
    SUFFIX=$2

    result="$PREFIX${SUFFIX}"
    result="${!result}"
    echo $result
}
